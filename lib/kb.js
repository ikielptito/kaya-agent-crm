// onboarding welcome, follow-ups, broadcasts) only goes out inside it; added
// outside, the welcome is deferred to the 9am WITA cron. Agent-INITIATED inbound
// is answered any time and does NOT consult this.
export function isWithinWitaHours(d = new Date()) {
  const witaHour = (d.getUTCHours() + 8) % 24;
  return witaHour >= 9 && witaHour < 21;
}

// Onboarding welcome template, in preference order (first match wins). A newer
// version rolls out automatically the moment Meta approves it — no code change,
// no downtime: until v2 is APPROVED the picker falls back to v1.
export const WELCOME_TEMPLATE_NAMES = ['samba_agent_welcome_v2', 'samba_agent_welcome_v1'];
export function pickWelcomeTemplate(templates, { requireApproved = true } = {}) {
  const list = Array.isArray(templates) ? templates : [];
  for (const name of WELCOME_TEMPLATE_NAMES) {
    const t = list.find(x => x.name === name && (!requireApproved || x.status === 'APPROVED'));
    if (t) return t;
  }
  return null;
}

